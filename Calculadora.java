public class Calculadora {
    public static void soma(double num1, double num2) {
     double sum = num1 + num2;
     System.out.println("O valor da soma é: " + sum);
    }
    public static void diminui(double num1, double num2) {
        double sum = num1 - num2;
        System.out.println("O valor da diferença é: " + sum);
       }
       public static void multiplica(double num1, double num2) {
        double sum = num1 * num2;
        System.out.println("O valor da multiplicação é: " + sum);
       }
       public static void divide(double num1, double num2) {
        double sum = num1 / num2;
        System.out.println("O valor da divisão é: " + sum);
       }
}
